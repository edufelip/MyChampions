/**
 * Mutation: Initialize usage statistics for a newly created template (admin only).
 * @param templateId The template ID to initialize stats for
 */
export async function initializeTemplateStats(
  client: DataConnectClient,
  templateId: string
): Promise<TemplateUsageStats> {
  const response = await client.mutation(
    `
    mutation InitializeTemplateStats($templateId: String!) {
      insert_template_usage_stats(
        data: {
          template_id: $templateId
          clone_count: 0
          assignment_count: 0
        }
      ) {
        template_id
        clone_count
        assignment_count
      }
    }
    `,
    { templateId }
  );

  const stats = (response as any)?.insert_template_usage_stats;
  return {
    templateId: stats.template_id,
    cloneCount: stats.clone_count,
    assignmentCount: stats.assignment_count,
  };
}
