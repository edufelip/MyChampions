/**
 * Query: Get template usage statistics.
 * Used to track popular templates and usage trends.
 * @param templateId The template ID to get stats for
 * @returns TemplateUsageStats object or null if not found
 */
export async function getTemplateStats(
  client: DataConnectClient,
  templateId: string
): Promise<TemplateUsageStats | null> {
  const response = await client.query(
    `
    query GetTemplateStats($templateId: String!) {
      template_usage_stats(where: { template_id: { eq: $templateId } }) {
        edges {
          node {
            template_id
            clone_count
            assignment_count
            updated_at
          }
        }
      }
    }
    `,
    { templateId }
  );

  const stats = (response as any)?.template_usage_stats?.edges?.[0]?.node;
  if (!stats) return null;

  return {
    templateId: stats.template_id,
    cloneCount: stats.clone_count,
    assignmentCount: stats.assignment_count,
  };
}

// ─── Mutation Operations ─────────────────────────────────────────────────────

/**
 * Mutation: Clone a starter template into a nutrition or training plan.
 * Implements: cloneStarterTemplate() stub
 * Links cloned plan to original template via source_template_id foreign key
 * @param professionalId The professional who owns the cloned plan
 * @param planName The name for the cloned plan
 * @param templateId The starter template ID to clone from
 * @param planType Whether to create 'nutrition' or 'training' plan
 * @returns The newly created plan ID
 */
export async function cloneStarterTemplate(
  client: DataConnectClient,
  professionalId: string,
  planName: string,
  templateId: string,
  planType: PlanType
): Promise<string> {
  const newPlanId = `plan_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`;

  const mutation =
    planType === 'nutrition'
      ? `
    mutation CloneNutritionTemplate(
      $id: String!
      $professionalId: String!
      $sourceTemplateId: String!
      $name: String!
    ) {
      insert_nutrition_plans(
        data: {
          id: $id
          professional_id: $professionalId
          source_template_id: $sourceTemplateId
          name: $name
          is_draft: true
        }
      ) {
        id
        professional_id
        source_template_id
        name
        is_draft
        created_at
      }
    }
    `
      : `
    mutation CloneTrainingTemplate(
      $id: String!
      $professionalId: String!
      $sourceTemplateId: String!
      $name: String!
    ) {
      insert_training_plans(
        data: {
          id: $id
          professional_id: $professionalId
          source_template_id: $sourceTemplateId
          name: $name
          is_draft: true
        }
      ) {
        id
        professional_id
        source_template_id
        name
        is_draft
        created_at
      }
    }
    `;

  await client.mutation(mutation, {
    id: newPlanId,
    professionalId,
    sourceTemplateId: templateId,
    name: planName,
  });

  // Increment clone count
  await incrementTemplateCloneCount(client, templateId);

  return newPlanId;
}

/**
 * Mutation: Increment template clone count when a template is cloned.
 * Called automatically by cloneStarterTemplate()
 * @param templateId The template ID to increment
 */
export async function incrementTemplateCloneCount(
  client: DataConnectClient,
  templateId: string
): Promise<void> {
  await client.mutation(
    `
    mutation IncrementTemplateCloneCount($templateId: String!) {
      update_template_usage_stats(
        where: { template_id: { eq: $templateId } }
        data: { clone_count: { increment: 1 } }
      ) {
        template_id
        clone_count
        updated_at
      }
    }
    `,
    { templateId }
  );
}

