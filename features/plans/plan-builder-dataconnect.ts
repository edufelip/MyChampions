// ─── Data Connect Service Client ─────────────────────────────────────────────

/**
 * Initialize Data Connect client with Firebase configuration.
 * This will be auto-generated by Firebase Data Connect SDK.
 */
export interface DataConnectClient {
  query: (query: string, variables?: Record<string, unknown>) => Promise<unknown>;
  mutation: (mutation: string, variables?: Record<string, unknown>) => Promise<unknown>;
}

// ─── Query Operations ────────────────────────────────────────────────────────

/**
 * Query: Get all starter templates, optionally filtered by plan type.
 * Implements: filterStarterTemplates() from logic layer
 * @param planType Optional filter by 'nutrition' or 'training'
 * @returns Array of StarterTemplate objects
 */
export async function getStarterTemplates(
  client: DataConnectClient,
  planType?: PlanType
): Promise<StarterTemplate[]> {
  // This query is defined in dataconnect/connector/starter_templates.gql
  const response = await client.query(
    `
    query GetStarterTemplates($planType: String) {
      starter_templates(
        where: { plan_type: { eq: $planType } }
        orderBy: [{ name: ASC }]
      ) {
        edges {
          node {
            id
            plan_type
            name
            description
            created_at
            updated_at
          }
        }
      }
    }
    `,
    { planType }
  );

  // Parse and transform response
  const templates = ((response as any)?.starter_templates?.edges || []).map(
    (edge: any) => ({
      id: edge.node.id,
      planType: edge.node.plan_type as PlanType,
      name: edge.node.name,
      description: edge.node.description,
      createdAt: edge.node.created_at,
    })
  );

  return templates;
}

/**
 * Query: Get a single starter template by ID.
 * Implements: Template lookup for cloning operations
 * @param templateId The starter template ID to retrieve
 * @returns StarterTemplate object or null if not found
 */
export async function getStarterTemplate(
  client: DataConnectClient,
  templateId: string
): Promise<StarterTemplate | null> {
  const response = await client.query(
    `
    query GetStarterTemplate($id: String!) {
      starter_templates(where: { id: { eq: $id } }) {
        edges {
          node {
            id
            plan_type
            name
            description
            created_at
            updated_at
          }
        }
      }
    }
    `,
    { id: templateId }
  );

  const template = (response as any)?.starter_templates?.edges?.[0]?.node;
  if (!template) return null;

  return {
    id: template.id,
    planType: template.plan_type as PlanType,
    name: template.name,
    description: template.description,
    createdAt: template.created_at,
  };
}

